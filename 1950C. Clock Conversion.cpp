void solve()
{
    int hour, minute;
    char colon;
    cin >> hour >> colon >> minute;

    str period = (hour < 12) ? "AM" : "PM";

    if (hour == 0)
        hour = 12;
    else if (hour > 12)
        hour -= 12;

    cout << setw(2) << setfill('0') << hour << ":" << setw(2) << setfill('0') << minute << " " << period << endl;
}

